// This is a basic Flutter widget test.
//
// To perform an interaction with a widget in your test, use the WidgetTester
// utility in the flutter_test package. For example, you can send tap and scroll
// gestures. You can also use WidgetTester to find child widgets in the widget
// tree, read text, and verify that the values of widget properties are correct.

import 'package:flutter/material.dart';
import 'package:flutter_test/flutter_test.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:stravix_id/main.dart';
import 'package:stravix_id/core/constants/app_constants.dart';

void main() {
  testWidgets('App launches and shows splash screen', (
    WidgetTester tester,
  ) async {
    // Build our app and trigger a frame.
    await tester.pumpWidget(const ProviderScope(child: StravixApp()));

    // Wait for initial frame
    await tester.pump();

    // Verify that the app name appears (STRAVIX.ID should be visible)
    expect(find.textContaining(AppConstants.appName), findsWidgets);
  });

  testWidgets('App has correct title', (WidgetTester tester) async {
    await tester.pumpWidget(const ProviderScope(child: StravixApp()));

    await tester.pump();

    // Verify app title
    final MaterialApp app = tester.widget(find.byType(MaterialApp));
    expect(app.title, AppConstants.appDisplayName);
  });
}
